-- NERDTree keybindings

-- n -> NERDTree
vim.keymap.set("n", "<leader>n",  ":NERDTreeFocus<CR>")
-- ns -> NERDTree Start
vim.keymap.set("n", "<leader>ns", ":NERDTree<CR>")
-- t -> Toggle
vim.keymap.set("n", "<leader>t",  ":NERDTreeToggle<CR>")
-- f -> Find
vim.keymap.set("n", "<leader>f",  ":NERDTreeFind<CR>")

